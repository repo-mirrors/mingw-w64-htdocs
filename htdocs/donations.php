trainees, several of these tasks will make good internship.
</p>

<h3>Donations</h3>
<p>
With the current organization and funds of mingw-w64, it wouldn't make sense to 
try to pay a developer to spend time on specific topics. Instead, the money 
will be spent on hardware (testing servers, Windows licenses, phone, tablets, 
...) and meeting expenses (setting up hackatons and paying for travel expenses 
as some major mingw-w64 developers live very far from others).
</p>

