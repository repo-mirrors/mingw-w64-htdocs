<h2 id="source">Source</h2>
<div class="toolchain">
  <p id="source-crt">
    Tarballs for the mingw-w64 sources are hosted on <a href="http://sourceforge.net/projects/mingw-w64/files/mingw-w64/mingw-w64-release/">SourceForge</a>. The latest version is <a href="http://sourceforge.net/projects/mingw-w64/files/mingw-w64/mingw-w64-release/mingw-w64-v3.1.0.tar.bz2/download">3.1.0</a>.
  </p>
  <p id="source-winpthreads">
    Winpthreads is available in the main tarball as of 3.1.0. For previous versions of the CRT, you need to <a href="http://sourceforge.net/p/mingw-w64/code/6184/tree/trunk/mingw-w64-libraries/winpthreads/">checkout the SVN repository for mingw-w64-libraries</a>.
  </p>
  <p>
    The wiki has instructions for building <a href="http://sourceforge.net/apps/trac/mingw-w64/wiki/Native%20Win64%20compiler">native</a> and <a href="http://sourceforge.net/apps/trac/mingw-w64/wiki/Cross%20Win32%20and%20Win64%20compiler">cross</a> toolchains.
  </p>
  <p>Details on how to get the mingw-w64 code from SVN and an SVN-web viewer are available on <a href="http://sourceforge.net/p/mingw-w64/code/HEAD/tree/">SourceForge</a>.</p>
</div>

